
        public class Main
        {




            public static void main(String[] args) {
                // Creating a reference variable for a String object
                String myString;

                // Creating an actual String object and assigning it to the reference variable
                myString = "Hello, World!";

                // Using the reference variable to access and manipulate the object
                System.out.println(myString); // Output: Hello, World!

                // You can also change the value of the reference variable
                myString = "Java is awesome!";
                System.out.println(myString); // Output: Java is awesome!
            }
        }

